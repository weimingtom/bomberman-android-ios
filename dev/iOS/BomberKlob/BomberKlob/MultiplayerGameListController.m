- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{

    static NSString *CellIdentifier = @"Cell";
    
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    if (cell == nil) {
        cell = [[[UITableViewCell alloc] initWithStyle:UITableViewCellStyleDefault reuseIdentifier:CellIdentifier] autorelease];
    }
	int i=0;
	for (NSDictionary * key in gameList) {
		if (i == indexPath.row) {
			NSMutableString * game = [[NSMutableString alloc] initWithString:@"Map: "];
			[game appendString:[key valueForKey:@"map"]];
			[game appendString:@"  Name: "];
			[game appendString:[key valueForKey:@"name"]];
			[game appendString:@" Players: "];
			NSString * numberPlayer = [key valueForKey:@"playerNumberConnected"];
			cell.textLabel.text = [NSString stringWithFormat:@"%@ %@",game,numberPlayer];
;
		}
		i++;
	}
    
    
    return cell;
}

